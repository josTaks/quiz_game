class Personne {
  final String nom;
  final String prenom;
  final DateTime dateDeNaissance;
  final double poids;
  final bool sexe;
  final String nationalite;
  const Personne({
    required this.nom,
    required this.prenom,
    required this.dateDeNaissance,
    required this.poids,
    required this.sexe,
    required this.nationalite,
  });
}
